# casting to integer
print("# casting to integer")

a = int(1)   # equals 1
b = int(2.5) # equals 2
c = int("3") # equals 3

print(a)
print(b)
print(c)

# casting to float
print("\n# casting to float")

a = float(1); # equals 1.0
b = float(2.2) # equals 2.2
c = float("3") # equals 3.0
d = float("4.5") # equals 4.5

print(a)
print(b)
print(c)
print(d)

# casting to string
print("\n# casting to string")

a = str("str1") # equals "str1"
b = str(2) # equals "2"
c = str(3.5) # equals "3.5"

print(a)
print(b)
print(c)