# For in range
for i in range(3):
    print(i)

print('\n')

# For in range <start, end>
for i in range(1, 5):
    print(i)

print('\n')

# For i in range with 2 sequence
for i in range(1, 10, 2):
    print(i)

print('\n')

# The else keyword in for loop specifies a block of code to be executed when the loop is finished
for x in range(1, 6):
    print(x)
else:
    print("Loop finished")